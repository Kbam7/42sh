#include "ft_shell.h"

void ft_signal_quit(void)
{
	t_shell *s;

	s = ft_get_shell();
	if(s->fork_pid != 0)
	{
		free_t_shell(s);
		kill(s->fork_pid, SIGKILL);
	}
}
