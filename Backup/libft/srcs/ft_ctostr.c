#include "libft.h"

char	*ft_ctostr(char c)
{
	char	str[2];
	char	*ptr;

	ptr = ft_strnew(1);
	str[0] = c;
	str[1] = '\0';
	ptr = ft_strcpy(ptr, str);
	return (ptr);
}
